              <h3>Don Bosco</h3>

             <img src="images/donbosco.jpg" width="200px" height="200px" alt="">
            <div class="event_right">
               

              <p>Saint John Bosco, popularly known as Don Bosco(Italian for Father Bosco) was born at Becchi, in Piedmont, Italy on August 16, 1815. From a very young age, he had decided that he had been called to work for the poor boys of that era when Europe was under the grip of the Industrial Revolution, and many young people who came to the cities to study or in search of work fell an easy prey to the many social evils of the time.
<br>
<br>
After being ordained a priest of the Catholic Church in 1841, Don Bosco came to the rescue of these poor boys with his novel method of educating youth through total dedication and personal involvement in their lives and problems.
<br>
<br>
To ensure that his total dedication to their cause shone through his actions, he based his education on the three great principles of Reason, Religion and Loving-Kindness, loving those to whom he dedicated his life as a caring father, and doing everything possible for their welfare. Don Bosco was attuned to the needs of his society. He did not visualize education in isolation from the community within whose parameters it functioned. Vocational guidance, vocational training, job placement and follow-up were as integral to Don Bosco's scheme of things as they are in modern education.
<br>
<br>
The system of education that emerged from these principles of Don Bosco is popularly known as the Preventive System and is the system that is followed in Don Bosco University. Joseph Zoppi, the Swiss educationist said, "If there ever existed a method of education adapted to inspire confidence and love, it is the method of Don Bosco." The system aims at creating a generation of young men and women steeped not only in sound knowledge-based education but also in a strong value-based education for life.</p>
